#tipe data Boolean
sudah_menikah = True
print(sudah_menikah)

#tipe data String
text1 = "Ayo belajar"
text2 = 'Python'
versi  = '3'
print(text1, text2, '(' + versi + ')')

#tipe data Integer
usia = 24
print(usia)

#tipe data Float
lebar = 10.5
print(lebar)

#tipe data Complex
a = 5j
b = 10j
c = a + b
print(a, '+', b, '=', c)
